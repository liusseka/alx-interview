#!/usr/bin/python3
"""Making a Change module.
"""


def makeChange(coins, total):
    """DCalculates the minimum number of coins required to make
    a specified total, given a collection of coins with
    varying values..
    """
    if total <= 0:
        return 0
    rem = total
    coins_count = 0
    coin_idx = 0
    sorted_coins = sorted(coins, reverse=True)
    n = len(coins)
    while rem > 0:
        if coin_idx >= n:
            return -1
        if rem - sorted_coins[coin_idx] >= 0:
            rem -= sorted_coins[coin_idx]
            coins_count += 1
        else:
            coin_idx += 1
    return coins_count